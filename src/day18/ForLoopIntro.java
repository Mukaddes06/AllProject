package day18;

public class ForLoopIntro {
    public static void main(String[] args) {
        // for loop () has 3 seperated by;
        //first part initialization:
        // it will run only once when you enter the loop
        // second part termination:
        // loop will stop if its false
        // it will be checked everytime
        //third part increment or decrement to change condition ever
        // it willl run after each iteration


        for(int i =1; i <=5 ; i++){
            System.out.println(" i = " +i);
            System.out.println("Happy Birthday Roksana Birthday");
        }
        for (int counter =10; counter>0;counter--){
            System.out.println("counter = " + counter);
        }


    }
}
