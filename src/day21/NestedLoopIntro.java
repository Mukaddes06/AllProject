package day21;

public class NestedLoopIntro {
    public static void main(String[] args) {
        for (int x = 1; x <=3 ; x++) {
            System.out.println("ITERATION : " + x);
            for (int i = 0; i <=5 ; i++) {
                System.out.println(x + " ");
            }

            System.out.println();

        }

    }
}
