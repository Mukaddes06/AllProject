package day21;

public class NumberStairCase {
    public static void main(String[] args) {

        for (int x = 1; x <=10 ; x++) {
            for (int y= 1; y <=x ; y++) {
                System.out.print(y + " ");
            }
            System.out.println();
        }




        for (int i = 1; i <=1 ; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
        for (int i = 1; i <=2 ; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
        for (int i = 1; i <=3 ; i++) {
            System.out.print(i + " ");
        }
        System.out.println();
        for (int i = 1; i <=4 ; i++) {
            System.out.print(i + " ");
        }
        System.out.println();


    }
}
